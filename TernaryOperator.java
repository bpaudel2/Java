public class TernaryOperator{
    public static void main(String args[]){
        int x, y;
        x =10;
        //variable x = (expression) ? value if true : value if false
        y =(x==5)?20:30;
        System.out.println("The value of y is "+ y);
        y=(x==10)?20:30;
        System.out.println("The value of y is "+ y);
    }
}